
public class StringSearching {

    public static void main(String args[]) {

        // Searching method 
        String name = "Ashwani";
        System.out.println("indexOf" + " " + name.indexOf("A"));
        System.out.println("startwith" + " " + name.startsWith("A"));
        System.out.println("endwidth" + " " + name.endsWith("i"));
        System.out.println("charAt" + " " + name.charAt(2));
        System.out.println("contains" + " " + name.contains("Ash"));
        System.out.println("lastIndexof" + " " + name.lastIndexOf("a"));

    }
}
